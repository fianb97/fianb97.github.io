const Utils = {
  // Generate unique ID
  id() {
    return Date.now().toString(36) + Math.random().toString(36).slice(2, 8);
  },

  // Format to Rupiah
  formatRupiah(amount, showSign = false) {
    const abs = Math.abs(amount);
    const formatted = new Intl.NumberFormat('id-ID').format(abs);
    const sign = showSign ? (amount >= 0 ? '+' : '-') : (amount < 0 ? '-' : '');
    return `${sign}Rp ${formatted}`;
  },

  // Short format (1jt, 500rb)
  formatShort(amount) {
    const abs = Math.abs(amount);
    if (abs >= 1000000) return `${(abs / 1000000).toFixed(1).replace('.0', '')}jt`;
    if (abs >= 1000) return `${(abs / 1000).toFixed(0)}rb`;
    return abs.toString();
  },

  // Parse Rupiah input string to number
  parseRupiah(str) {
    if (typeof str === 'number') return str;
    return parseInt(String(str).replace(/[^0-9]/g, ''), 10) || 0;
  },

  // Format date
  formatDate(dateStr) {
    const d = new Date(dateStr);
    return d.toLocaleDateString('id-ID', { day: 'numeric', month: 'short', year: 'numeric' });
  },

  // Format relative date
  formatRelativeDate(dateStr) {
    const d = new Date(dateStr);
    const now = new Date();
    const diffMs = now - d;
    const diffDays = Math.floor(diffMs / (1000 * 60 * 60 * 24));
    if (diffDays === 0) return 'Hari ini';
    if (diffDays === 1) return 'Kemarin';
    if (diffDays < 7) return `${diffDays} hari lalu`;
    return Utils.formatDate(dateStr);
  },

  // Format date for grouping
  formatDateGroup(dateStr) {
    const d = new Date(dateStr);
    const now = new Date();
    const today = new Date(now.getFullYear(), now.getMonth(), now.getDate());
    const target = new Date(d.getFullYear(), d.getMonth(), d.getDate());
    const diffDays = Math.floor((today - target) / (1000 * 60 * 60 * 24));
    if (diffDays === 0) return 'Hari Ini';
    if (diffDays === 1) return 'Kemarin';
    return d.toLocaleDateString('id-ID', { weekday: 'long', day: 'numeric', month: 'long', year: 'numeric' });
  },

  // Get today's date as YYYY-MM-DD
  today() {
    return new Date().toISOString().split('T')[0];
  },

  // Get current month & year label
  currentMonthLabel() {
    return new Date().toLocaleDateString('id-ID', { month: 'long', year: 'numeric' });
  },

  // Get long date string
  longDate() {
    return new Date().toLocaleDateString('id-ID', { weekday: 'long', day: 'numeric', month: 'long', year: 'numeric' });
  },

  // Check if date is in current month
  isCurrentMonth(dateStr) {
    const d = new Date(dateStr);
    const now = new Date();
    return d.getMonth() === now.getMonth() && d.getFullYear() === now.getFullYear();
  },

  // Get start of current week (Monday)
  startOfWeek() {
    const now = new Date();
    const day = now.getDay();
    const diff = now.getDate() - day + (day === 0 ? -6 : 1);
    const monday = new Date(now.setDate(diff));
    return monday.toISOString().split('T')[0];
  },

  // Escape HTML to prevent XSS
  escapeHtml(str) {
    const div = document.createElement('div');
    div.textContent = str;
    return div.innerHTML;
  },

  // Debounce
  debounce(fn, delay = 300) {
    let timer;
    return (...args) => {
      clearTimeout(timer);
      timer = setTimeout(() => fn(...args), delay);
    };
  }
};

